export function resolveIcon(kebabName) {
  if (!kebabName) return CircleDot
  const pascal = kebabName
    .split('-')
    .map(s => s.charAt(0).toUpperCase() + s.slice(1))
    .join('')
  return iconMap[pascal] || CircleDot
}
